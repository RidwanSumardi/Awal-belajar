fun main(){
//    Kotlin Sementara Loop
//Perulangan whilemelakukan perulangan melalui blok kode selama kondisi yang ditentukan adalah true:

    var i = 0
    while (i <  10){
        println("$i Saya akan mengulangnya jika masih belum sampai")
        i++
    }
    /*Perulangan menggunakan do / while*/
    var s = 0
    do {
        println(s)
        s++
    }while (s < 8)
}