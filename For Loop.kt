fun main(){
    /*
    Seringkali ketika Anda bekerja dengan array, Anda perlu mengulang semua elemen.
    Untuk melakukan loop melalui elemen array, gunakan for loop bersama dengan inoperator
     */
    val mobile = arrayOf("Fortuner","Alphad","Feray")
    for (kendaraan in mobile){
        println(kendaraan)
    }
    /*
    mengunakan Int dalam for
    contohnya:
     */
    val no = arrayOf(1,23,100,200)
    for (urut in no){
        println(urut)
    }
}